// Build script: generates ../melvor-save.js (a build-free browser bundle) from
// the vendored MelvorParse reader/writer TypeScript sources.
//
// Pipeline: read vendor/*.ts -> Node stripTypeScriptTypes -> drop imports/exports
// -> wrap in an IIFE that provides a pako shim (replacing fflate), a lossless
// Map/Set/ArrayBuffer <-> JSON layer, and a small public API.
//
// Run with: node tools/build-melvor-save.js
//
// The save FORMAT decoded here is Melvor Idle's; the field-by-field schema was
// adapted from the open-source MelvorParse project by Shuttleu
// (https://github.com/Shuttleu/MelvorParse). See README for licensing notes.

const fs = require("fs");
const path = require("path");
const { stripTypeScriptTypes } = require("node:module");

const vendorDir = path.join(__dirname, "vendor");
const outFile = path.join(__dirname, "..", "melvor-save.js");

function stripFile(name) {
    const tsCode = fs.readFileSync(path.join(vendorDir, name), "utf8");
    let js = stripTypeScriptTypes(tsCode, { mode: "strip" });
    // Drop ES module imports (fflate + ./type) and the `export` keywords.
    js = js
        .split(/\r?\n/)
        .filter((line) => !/^\s*import\s.+from\s.+$/.test(line))
        .join("\n")
        .replace(/^\s*export\s+/gm, "");
    return js;
}

const readerJs = stripFile("reader.ts");
const writerJs = stripFile("writer.ts");

const header = `// melvor-save.js -- decode/edit/re-encode Melvor Idle save exports.
// AUTO-GENERATED by tools/build-melvor-save.js -- do not edit by hand.
//
// New Melvor save format (v130/v131): base64( zlib-deflate( ordered binary stream ) ).
// The binary schema was adapted from MelvorParse by Shuttleu
// (https://github.com/Shuttleu/MelvorParse). Requires pako (loaded separately).
//
// Public API (global \`MelvorSave\`):
//   MelvorSave.decodeToJSON(saveString) -> pretty JSON text (throws on bad input)
//   MelvorSave.encodeFromJSON(jsonText) -> save export string
//   MelvorSave.decode(saveString)       -> { saveData, initialSize }
//   MelvorSave.encode(saveData, size)   -> save export string
//
// In the JSON view, binary Maps/Sets/byte-buffers are tagged so editing is
// lossless and round-trips byte-for-byte:
//   a Map  -> { "#map":  [[key, value], ...] }   (order + numeric keys preserved)
//   a Set  -> { "#set":  [value, ...] }
//   bytes  -> { "#bytes": [0..255, ...] }
`;

const prelude = `(function (root) {
    "use strict";

    var pako = root.pako || (typeof require === "function" ? require("./pako.min.js") : undefined);
    if (!pako) throw new Error("MelvorSave: pako library not found (load pako.min.js first).");

    // --- fflate -> pako shims (the vendored code calls these names) ---
    function strToU8(str) {
        var u8 = new Uint8Array(str.length);
        for (var i = 0; i < str.length; i++) u8[i] = str.charCodeAt(i) & 0xff;
        return u8;
    }
    function strFromU8(u8) {
        var s = "", CH = 0x8000;
        for (var i = 0; i < u8.length; i += CH)
            s += String.fromCharCode.apply(null, u8.subarray(i, Math.min(i + CH, u8.length)));
        return s;
    }
    function unzlibSync(u8) { return pako.inflate(u8); }
    function zlibSync(u8) { return pako.deflate(u8); }
`;

const jsonLayerAndApi = `
    // --- lossless tagged JSON <-> saveData (preserves Map/Set/byte buffers) ---
    function replacer(key, value) {
        if (value instanceof Map) return { "#map": Array.from(value.entries()) };
        if (value instanceof Set) return { "#set": Array.from(value.values()) };
        if (value instanceof ArrayBuffer) return { "#bytes": Array.from(new Uint8Array(value)) };
        if (ArrayBuffer.isView(value) && !(value instanceof DataView)) return { "#bytes": Array.from(value) };
        return value;
    }
    function reviver(key, value) {
        if (value && typeof value === "object" && !Array.isArray(value)) {
            if (Object.prototype.hasOwnProperty.call(value, "#map")) return new Map(value["#map"]);
            if (Object.prototype.hasOwnProperty.call(value, "#set")) return new Set(value["#set"]);
            if (Object.prototype.hasOwnProperty.call(value, "#bytes")) return Uint8Array.from(value["#bytes"]);
        }
        return value;
    }

    function decodeToJSON(saveString) {
        var result = parseString(String(saveString).trim());
        if (typeof result === "string") throw new Error(result); // reader returns its error message as a string
        return JSON.stringify(result.saveData, replacer, 2);
    }
    function encodeFromJSON(jsonText) {
        var saveData = JSON.parse(jsonText, reviver);
        // jsonText length is a safe over-estimate of the binary size; the writer
        // also grows its buffer geometrically if needed.
        return parseSave(saveData, jsonText.length + 1024);
    }

    var api = {
        decodeToJSON: decodeToJSON,
        encodeFromJSON: encodeFromJSON,
        decode: parseString,
        encode: parseSave,
        // object <-> tagged-JSON-text helpers (share the Map/Set/bytes tagging)
        toJSON: function (saveData) { return JSON.stringify(saveData, replacer, 2); },
        fromJSON: function (jsonText) { return JSON.parse(jsonText, reviver); }
    };
    root.MelvorSave = api;
    if (typeof module !== "undefined" && module.exports) module.exports = api;
})(typeof window !== "undefined" ? window : (typeof globalThis !== "undefined" ? globalThis : this));
`;

const bundle = [
    header,
    prelude,
    "\n    // ===== reader (vendored, type-stripped) =====\n",
    indent(readerJs),
    "\n    // ===== writer (vendored, type-stripped) =====\n",
    indent(writerJs),
    jsonLayerAndApi
].join("\n");

function indent(code) {
    return code.split(/\r?\n/).map((l) => (l.length ? "    " + l : l)).join("\n");
}

fs.writeFileSync(outFile, bundle, "utf8");
console.log("Wrote " + outFile + " (" + bundle.length + " bytes)");
